def parse_chalisa(raw_text):
    """Returns list of (section, segment_type, text) tuples."""
    section = "chaupai"
    segments = []
    # Walk the text token by token, splitting on '॥' but pulling out header
    # markers like "॥ दोहा ॥" first via regex substitution to sentinel.
    text = raw_text.strip()

    # Replace header markers with a sentinel that records the section change.
    def header_sub(m):
        word = m.group(1)
        return f"\n@@SECTION:{word}@@\n"

    text = HEADER_RE.sub(header_sub, text)

    # Now split remaining text on danda marks '॥' (keep them attached to
    # the preceding segment for readability, matching devotional convention).
    # First split on our section sentinels while preserving order.
    parts = re.split(r"(@@SECTION:[^\@]+@@)", text)
    for part in parts:
        part = part.strip()
        if not part:
            continue
        sec_match = re.match(r"@@SECTION:([^\@]+)@@", part)
        if sec_match:
            section = SECTION_TO_TYPE.get(sec_match.group(1), "chaupai")
            continue
        # Split this chunk into couplets on '॥'
        chunks = [c.strip() for c in part.split("॥") if c.strip()]
        for c in chunks:
            # Normalise whitespace/newlines within a couplet to single spaces.
            c = re.sub(r"\s+", " ", c).strip()
            if c:
                segments.append((section, section, c + "॥"))
    return segments


